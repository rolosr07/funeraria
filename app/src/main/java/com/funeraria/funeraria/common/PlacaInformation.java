public class PlacaInformation {

    private String nombre;
    private String apellidos;
    private String fechaNacimiento;
    private String fechaDefuncion;
    private String imagenSuperior;
    private String imagenOrla;
    private String esquela;
    private String nombreLugarRestos;
    private String ubicacionLugarRestos;

    public PlacaInformation(){}

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellidos() {
        return apellidos;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    public String getFechaNacimiento() {
        return fechaNacimiento;
    }

    public void setFechaNacimiento(String fechaNacimiento) {
        this.fechaNacimiento = fechaNacimiento;
    }

    public String getFechaDefuncion() {
        return fechaDefuncion;
    }

    public void setFechaDefuncion(String fechaDefuncion) {
        this.fechaDefuncion = fechaDefuncion;
    }

    public String getImagenSuperior() {
        return imagenSuperior;
    }

    public void setImagenSuperior(String imagenSuperior) {
        this.imagenSuperior = imagenSuperior;
    }

    public String getImagenOrla() {
        return imagenOrla;
    }

    public void setImagenOrla(String imagenOrla) {
        this.imagenOrla = imagenOrla;
    }

    public String getEsquela() {
        return esquela;
    }

    public void setEsquela(String esquela) {
        this.esquela = esquela;
    }

    public String getNombreLugarRestos() {
        return nombreLugarRestos;
    }

    public void setNombreLugarRestos(String nombreLugarRestos) {
        this.nombreLugarRestos = nombreLugarRestos;
    }

    public String getUbicacionLugarRestos() {
        return ubicacionLugarRestos;
    }

    public void setUbicacionLugarRestos(String ubicacionLugarRestos) {
        this.ubicacionLugarRestos = ubicacionLugarRestos;
    }
}
